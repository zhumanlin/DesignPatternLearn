package pattern.bridgePattern;

public class SingletonCircle extends Shape {
    private int x, y, radius;

    protected SingletonCircle(int x, int y, int radius, DrawAPI drawAPI) {
        super(drawAPI);
        this.x = x;
        this.y = y;
        this.radius = radius;
    }

    @Override
    public void draw() {
        drawAPI.drawCircle(radius,x,y);
    }
}
